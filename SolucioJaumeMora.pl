	recomanarLlibres(LlistaIdiomes, RespostaFinal, AnyMinim, LlibresRecomanats) :-
		findall([Títol, Autor, Any, Gènere, Subgènere, Temàtica, Editorial, Idioma], llibre(Títol, Autor, Any, Gènere, Subgènere, Temàtica, Editorial, Idioma), Llibres),
		recomanarLlibresAux(LlistaIdiomes, RespostaFinal, AnyMinim, Llibres, LlibresRecomanats).

	recomanarLlibresAux(_, _, _, [], []).

	recomanarLlibresAux(LlistaIdiomes, RespostaFinal, AnyMinim, [[Títol, Autor, Any, Gènere, Subgènere, Temàtica, Editorial, Idioma]|RestaLlibres], LlibresRecomanats) :-
		(member(Idioma, LlistaIdiomes), member(Temàtica, RespostaFinal), Any >= AnyMinim -> 
			recomanarLlibresAux(LlistaIdiomes, RespostaFinal, AnyMinim, RestaLlibres, LlibresRecomanats2),
			append([[Títol, Autor, Any, Gènere, Subgènere, Temàtica, Editorial, Idioma]], LlibresRecomanats2, LlibresRecomanats)
		;
			recomanarLlibresAux(LlistaIdiomes, RespostaFinal, AnyMinim, RestaLlibres, LlibresRecomanats)
		).
	
    
	mostrarLlibres(LlibresRecomanats) :-
		random_permutation(LlibresRecomanats, LlibresRecomanatsRandom),
		mostrarLlibresAux(LlibresRecomanatsRandom, 1).
	
	mostrarLlibresAux([], _).
	
	mostrarLlibresAux([[Títol, Autor, Any, Gènere, Subgènere, Temàtica, Editorial, Idioma]|RestaLlibres], NumLlibres) :-
		NumLlibres < 6 -> 
			write('LLIBRE '), write(NumLlibres), nl,
			write('Títol: '), write(Títol), nl,
			write('Autor: '), write(Autor), nl,
			write('Any: '), write(Any), nl,
			write('Gènere: '), write(Gènere), nl,
			write('Subgènere: '), write(Subgènere), nl,
			write('Temàtica: '), write(Temàtica), nl,
			write('Editorial: '), write(Editorial), nl,
			write('Idioma: '), write(Idioma), nl, nl, nl,
			
			write('Vols saber perquè t''hem recomanat llegir aquest llibre?'), nl,
			write('1. Sí'), nl,
			write('2. No'), nl,
			read(Recomanacio), nl,

			(Recomanacio == 1 -> 
				write('Hola, se t''ha recomanat '), write(Títol), write(' perquè és un llibre de '), write(Gènere), write(' i '), write(Subgènere), write(' que tracta sobre '), write(Temàtica), write('. Recorda que aquesta temàtica estava inclosa dins de les teves preferències i no la tenies pas descartada. A més a més, està escrit en '), write(Idioma), write(', idioma que has dit que saps parlar. Finalment, si t''hi fixesm també compleix el requisit de l''any. Segur que t''agradarà!'), nl, nl, nl
			;
				true
			),

			NumLlibres2 is NumLlibres + 1,
			mostrarLlibresAux(RestaLlibres, NumLlibres2)
		;
			true.

	guardarUsuari(IdUsuari, Nom, Edat, RespostaFinal, LlistaIdiomes, AnyMinim) :-
		open('usuari_dades_JMora.txt', append, Stream),
		writeq(Stream, usuari(IdUsuari, Nom, Edat, RespostaFinal, LlistaIdiomes, AnyMinim)), write(Stream, '.\n'),
		close(Stream).

	consultarUsuari(IdUsuariBuscat, Nom, Edat, RespostaFinal, LlistaIdiomes, AnyMinim) :-
		open('usuari_dades_JMora.txt', read, Stream),
		buscarUsuari(Stream, IdUsuariBuscat, Nom, Edat, RespostaFinal, LlistaIdiomes, AnyMinim),
		close(Stream).

	buscarUsuari(Stream, IdUsuariBuscat, Nom, Edat, RespostaFinal, LlistaIdiomes, AnyMinim) :-
		read(Stream, Term),
		(   Term == end_of_file
		->  (Nom, Edat, RespostaFinal, LlistaIdiomes, AnyMinim) = (not_found, not_found, not_found, not_found, not_found)
		;   Term = usuari(IdUsuari, Nom, Edat, RespostaFinal, LlistaIdiomes, AnyMinim),
			(   IdUsuariBuscat == IdUsuari
			->  true
			;   buscarUsuari(Stream, IdUsuariBuscat, Nom, Edat, RespostaFinal, LlistaIdiomes, AnyMinim)
			)
		).